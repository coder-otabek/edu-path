window.getCurrentLang = function() {
  return localStorage.getItem('edupath_lang') ||
         document.documentElement.getAttribute('lang') || 'uz';
};

window.t = function(key) {
  const lang = window.getCurrentLang();
  return (window.TRANSLATIONS[lang] && window.TRANSLATIONS[lang][key]) ||
         (window.TRANSLATIONS.uz && window.TRANSLATIONS.uz[key]) || key;
};

window.applyTranslations = function() {
  const lang = window.getCurrentLang();
  const dict = window.TRANSLATIONS[lang] || window.TRANSLATIONS.uz;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = dict[el.getAttribute('data-i18n')];
    if (!val) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else if (el.tagName === 'OPTION') {
      el.textContent = val;
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const val = dict[el.getAttribute('data-i18n-ph')];
    if (val) el.placeholder = val;
  });
  // Placeholder atributlarini ham tarjima qilamiz
  document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph') || el.getAttribute('data-i18n');
    if (key && dict[key]) el.placeholder = dict[key];
  });
};

window.setLang = function(lang) {
  localStorage.setItem('edupath_lang', lang);
  document.documentElement.setAttribute('lang', lang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.style.borderColor = active ? 'var(--primary)' : 'var(--border)';
    btn.style.background  = active ? 'var(--primary-light)' : 'transparent';
    btn.style.color       = active ? 'var(--primary)' : 'var(--text-secondary)';
  });
  // Avval tarjimani qo'llaymiz
  window.applyTranslations();
  // Serverga saqlaymiz va sahifani reload qilamiz
  const csrf = document.cookie.match(/csrftoken=([^;]+)/)?.[1] || '';
  fetch('/accounts/settings/', {
    method: 'POST',
    headers: {'Content-Type':'application/x-www-form-urlencoded','X-CSRFToken': csrf},
    body: 'action=language&language=' + lang
  }).then(() => {
    // Server saqladi — sahifani yangilaymiz (to'liq tarjima uchun)
    window.location.reload();
  }).catch(() => {
    // Server ishlamasa ham reload qilamiz
    window.location.reload();
  });
};

document.addEventListener('DOMContentLoaded', window.applyTranslations);
